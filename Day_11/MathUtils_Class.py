class MathUtils:
    @staticmethod
    def add(a, b):
        return a + b

    @staticmethod
    def subtract(a, b):
        return a - b

    @staticmethod
    def multiply(a, b):
        return a * b

print(MathUtils.add(10, 5))       # 15
print(MathUtils.subtract(10, 5))  # 5
print(MathUtils.multiply(4, 3))   # 12
